package day15_String;

public class String_Methods {

    public static void main(String[] args) {

        String name = "Cybertek";
        // charAt(index)
        char ch1 = name.charAt(0); // 'C'
        System.out.println(ch1);
        char ch2 = name.charAt(5); // 't'
        System.out.println(ch2);
    }
}
