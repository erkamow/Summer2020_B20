package day21_do_WhileLoop;

public class doWhile_practice {

    public static void main(String[] args) {
         int num = 1;
        do{
            System.out.print(num + " ");

            num++;

        }while(num <= 20);


        System.out.println("=======================================");

        char ch = 'Z';

        do{
            System.out.print(ch + " ");
            ch--;
        }while(ch >= 'A');



        }

    }

