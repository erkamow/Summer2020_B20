package day03_sequences_variables;

public class EscapeSequences {


    public static void main(String[] args) {


        System.out.println("Hello Eveyone \n\tMy Name Is Cybertek");
        System.out.println("\tI am in adapazari");
        System.out.println("\n\n\t\t\ti love programming");
        System.out.println("\\");     // this prints me one back slash
        System.out.println("\\\\");   // this prints me two back slash
        System.out.println("/");      // this prints me one front slash


        System.out.println("my name is \'Erkam\'");
        System.out.println("my name is 'Erkam'");
        System.out.println("my favorite tv-series: \"Money Heist\"");
        System.out.println("Hello my name is 'Erkam'\n\tMy favorite tv-series is \"Money Heist\"\n\tMy favorite book is\"black Rose\"");

        System.out.println("        ^");
        System.out.println("      /   \\");
        System.out.println("     /     \\");
        System.out.println("    /       \\");
        System.out.println("   /_________\\");
        System.out.println("   \\         /");
        System.out.println("    \\       /");
        System.out.println("     \\     /");
        System.out.println("      \\   /");
        System.out.println("        v");

    }
}
